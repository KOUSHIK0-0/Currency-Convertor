let BASE_URL = 'https://v6.exchangerate-api.com/v6/550316e2c7df1f69ba9df741/latest/'

const scountries = document.querySelectorAll('.select_countries')

console.log(scountries[0])



for(select_btn of scountries)
{
    for(countryCode in countryList)
    {
        const new_country = document.createElement('option')
        new_country.innerText = countryCode
        new_country.value = countryCode
        if(select_btn.name === "from" && countryCode === "USD")
        {
            new_country.selected = "selected"
        }
        else if(select_btn.name === "to" && countryCode === "INR")
        {
            new_country.selected = "selected"
        }
        select_btn.appendChild(new_country)
    }
    select_btn.addEventListener('change',(e)=>{
        const countryCode = e.target.value
        const country = countryList[countryCode]
        const img = e.target.parentElement.querySelector("img")
        img.src = `https://flagsapi.com/${country}/flat/64.png`
    })
}

const ExchangeRate = async () => {

    let amount = document.getElementById('amount').value
    if(amount === "" || amount < 1)
    {
        amount = 1
        amount.value = "1"
    }
    let fromcountry = document.querySelector('.from .select_countries').value
    let tocountry = document.querySelector('.to .select_countries').value
    let result = document.querySelector('.msg')
    const url = BASE_URL + fromcountry
    const response = await fetch(url)
    const body = await response.json()
    console.log(body["conversion_rates"][tocountry])
    console.log(amount)
    result.innerText = `${amount}${fromcountry}=${amount * body["conversion_rates"][tocountry]}${tocountry}`
}


document.querySelector('.btn').addEventListener('click',async (e)=>{
    ExchangeRate()
});






